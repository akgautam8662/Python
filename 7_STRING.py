# STRINGS:- Strings in python are surrounded by either single quotation marks, or double quotation marks.

# String are immutable

# 'hello' is the same as "hello".
print('hello')
print("hello")

# MULTILINE STRING : 

str = '''Johny, Johny? Yes, Papa?
Eating sugar? No, Papa
Are you sure? Yes, Papa
Open your mouth, ha, ha, ha'''
print(str)

# STRING ARRAY :
# In Python , string is like array of a characters.. 

a="january"
print(a[0])
print(a[1])
print(a[2])
print(a[6])

# LOOPING THROUGH A STRING : 

st="Hello Coder"
for st in st:
    print(st)


# STRING LENGTH :
# To get the length of a string, use the len() function...

y="ashish kumar gautam" 
print(len(y))


