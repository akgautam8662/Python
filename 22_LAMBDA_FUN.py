# LAMBDA FUNCTION : 

# A lambda function is a small anonymous function.

# A lambda function can take any number of arguments, but can only have one expression.

def sum(a,b):
    return a+b

print(sum(5,7))


sum=lambda a,b :a+b 
print(sum(12,8))

avg=lambda x,y:(x+y)/2
print(avg(5,5))
